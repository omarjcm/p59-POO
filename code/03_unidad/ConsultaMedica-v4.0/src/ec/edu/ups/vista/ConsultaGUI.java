    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        Consulta consulta = new Consulta();
        Paciente paciente = consulta.getRefPaciente();
        Especialidad especialidad = consulta.getRefEspecialidad();
        
        paciente.setNombre( this.txtNombre.getText() );
        paciente.setApellido( this.txtApellido.getText() );
        if (this.rbtMasculino.isSelected()) {
            paciente.setSexo("M");
        } else if (this.rbtFemenino.isSelected()) {
            paciente.setSexo("F");
        }
        paciente.setAnioNacimiento( this.txtAnioNacimiento.getText() );
        
        String texto = this.lstEspecialidades.getSelectedValue();
        for (Especialidad temp : this.consulta.getRefGestionarEspecialidad().getRefEspecialidades()) {
            if (temp.getNombre().equals( texto )) {
                especialidad = new Especialidad( temp );
            }
        }
        this.consulta.getRefGestionarConsulta().registrar( consulta );
        this.setVisible(false);
    }//GEN-LAST:event_btnGuardarActionPerformed


